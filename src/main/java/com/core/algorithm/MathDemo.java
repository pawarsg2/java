package com.core.algorithm;
import java.lang.*;

public class MathDemo {

    public static void main(String[] args) {

        // get two integer numbers
        int x = 60984;
        int y = 1000;

        // print the larger number between x and y
        System.out.println("Math.max(" + x + "," + y + ")=" + Math.max(x, y));
    }
}